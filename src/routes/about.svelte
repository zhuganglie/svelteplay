     <p>我喜欢读书。书店是常去的地方。刚开始工作那会儿，收入微薄，生活捉襟见肘，但每月仍会拿出一部分收入用来购买书籍。到我辞职的时候，家里的书房都快堆成了迷你图书馆。我的阅读范围并不广，仅局限于哲学、社会科学和科普，对小说完全没有兴趣。《红楼梦》读几行就想睡觉，《自杀论》却可以读通宵而不知疲倦。得益于阅读，我能清楚地理解抽象的哲学概念，也能处理棘手的组织管理问题，还能欣赏诗词歌赋中蕴涵的意境。我是读者，也是作者，有若干专业文章发表。我的作品看的人不多，但看了的都说好。</p>

    <p>除了读书学习，我还喜欢远足，喜欢骑脚踏车，喜欢听古典音乐，喜欢品茶，喜欢做美食......。我最大的嗜好是下围棋，从大学一年级开始一直下到现在，从未间断。作为策略游戏，围棋让我领悟了不少博弈之道，让我在生活的博弈中游刃有余。</p>

